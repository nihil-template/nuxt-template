const state = {
  current: null,
  history: [],
};

const ANGRA_QUEST_ROLLS = new Set([32, 33, 34, 35, 36, 37, 38, 39]);

const elements = {
  rollButton: document.querySelector("#rollButton"),
  copyButton: document.querySelector("#copyButton"),
  angraToggle: document.querySelector("#angraToggle"),
  questRoll: document.querySelector("#questRoll"),
  questTitle: document.querySelector("#questTitle"),
  questText: document.querySelector("#questText"),
  themeRoll: document.querySelector("#themeRoll"),
  themeTitle: document.querySelector("#themeTitle"),
  themeText: document.querySelector("#themeText"),
  monsterText: document.querySelector("#monsterText"),
  terrainRoll: document.querySelector("#terrainRoll"),
  terrainTitle: document.querySelector("#terrainTitle"),
  terrainText: document.querySelector("#terrainText"),
  sessionHook: document.querySelector("#sessionHook"),
  historyList: document.querySelector("#historyList"),
  cards: document.querySelectorAll(".result-card"),
};

function pick(table) {
  const index = Math.floor(Math.random() * table.length);
  return table[index];
}

function getQuestPool() {
  if (elements.angraToggle.checked) {
    return questTable;
  }

  return questTable.filter((quest) => !ANGRA_QUEST_ROLLS.has(quest.roll));
}

function formatMonsterList(theme) {
  return theme.monsters.join(" · ");
}

function buildHook(quest, theme, terrain) {
  return (
    "[" +
    quest.roll +
    ", " +
    theme.roll +
    ", " +
    terrain.roll +
    "] " +
    quest.title +
    " + " +
    theme.title +
    " @ " +
    terrain.title +
    ": " +
    quest.text +
    " 전장은 " +
    terrain.text +
    " 조우 테마는 " +
    theme.text +
    " 몬스터 예시: " +
    formatMonsterList(theme) +
    "."
  );
}

function renderResult(quest, theme, terrain) {
  const hook = buildHook(quest, theme, terrain);
  state.current = { quest, theme, terrain, hook };
  state.history.unshift(state.current);
  state.history = state.history.slice(0, 6);

  elements.questRoll.textContent = quest.roll;
  elements.questTitle.textContent = quest.title;
  elements.questText.textContent = quest.text;

  elements.themeRoll.textContent = theme.roll;
  elements.themeTitle.textContent = theme.title;
  elements.themeText.textContent = theme.text;
  elements.monsterText.textContent = "몬스터 예시: " + formatMonsterList(theme);

  elements.terrainRoll.textContent = terrain.roll;
  elements.terrainTitle.textContent = terrain.title;
  elements.terrainText.textContent = terrain.text;

  elements.sessionHook.textContent = hook;
  elements.copyButton.disabled = false;

  elements.cards.forEach((card) => {
    card.classList.remove("roll-flash");
    void card.offsetWidth;
    card.classList.add("roll-flash");
  });

  renderHistory();
}

function renderHistory() {
  elements.historyList.innerHTML = "";

  state.history.forEach((item) => {
    const entry = document.createElement("li");
    entry.textContent =
      "[" +
      item.quest.roll +
      "/" +
      item.theme.roll +
      "/" +
      item.terrain.roll +
      "] " +
      item.quest.title +
      " · " +
      item.theme.title +
      " · " +
      item.terrain.title;
    elements.historyList.append(entry);
  });
}

async function copyCurrentResult() {
  if (!state.current) return;

  try {
    if (navigator.clipboard) {
      await navigator.clipboard.writeText(state.current.hook);
    } else {
      copyWithFallback(state.current.hook);
    }
    elements.copyButton.textContent = "복사 완료";
  } catch {
    try {
      copyWithFallback(state.current.hook);
      elements.copyButton.textContent = "복사 완료";
    } catch {
      elements.copyButton.textContent = "복사 실패";
    }
  }

  window.setTimeout(() => {
    elements.copyButton.textContent = "결과 복사";
  }, 1200);
}

function copyWithFallback(text) {
  const textarea = document.createElement("textarea");
  textarea.value = text;
  textarea.setAttribute("readonly", "");
  textarea.style.position = "fixed";
  textarea.style.left = "-9999px";
  document.body.append(textarea);
  textarea.select();
  document.execCommand("copy");
  textarea.remove();
}

elements.rollButton.addEventListener("click", () => {
  renderResult(pick(getQuestPool()), pick(themeTable), pick(terrainTable));
});

elements.copyButton.addEventListener("click", copyCurrentResult);
